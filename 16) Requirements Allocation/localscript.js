function bd_in(){
    var a = document.getElementsByClassName("bd");
    var i;
    for (i = 0; i < a.length; i++) {
      a[i].style.background = "#fbed6a";
    }
}

function bd_out(){
    var a = document.getElementsByClassName("bd");
    var i;
    for (i = 0; i < a.length; i++) {
      a[i].style.background = "#fefad6";
    }
}

